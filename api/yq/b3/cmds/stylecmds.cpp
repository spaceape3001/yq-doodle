#include <yq/b3/Parser.hpp>
#include <yq/b3/annotate/Style.hpp>

using namespace yq;
using namespace yq::b3;

////////////////////////////////////////////////////////////////////////////////

static bool cmdStyle(Parser& p, const ArgList& pArgs, const ArgMap& nArgs)
{
    if(pArgs.size() < 1){
        p.gripe("Style needs a name!");
        return false;
    }

    p << new Style({.attrs=nArgs, .frame=p.frame(), .name=pArgs.values[0]});
    return true;
}

YQ_B3_COMMAND("style", ifNone, 1, cmdStyle);

